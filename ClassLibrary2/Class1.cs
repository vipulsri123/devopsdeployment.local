using System;

namespace ClassLibrary1
{
    public class calculation
    {
        public Int32 sum(Int32 a, Int32 b)
        {
            return a + b;
        }
        public Int32 sub(Int32 a, Int32 b)
        {
            return a - b;
        }
        public Int32 mul(Int32 a, Int32 b)
        {
            return a * b;
        }
        public Int32 div(Int32 a, Int32 b)
        {
            return a / b;
        }
    }
}
